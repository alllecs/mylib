#include <stdio.h>
#include <stdlib.h>


unsigned int array[8][8] = {
{ 0, 0, 1, 3, 3, 0, 0, 1 },
{ 3, 0, 3, 1, 1, 1, 0, 3 },
{ 1, 0 ,0, 3, 0, 0, 0, 1 },
{ 0, 1, 0, 0, 3, 1, 0, 1 },
{ 0, 1, 3, 0, 0, 0, 1, 0 },
{ 3, 1, 3, 1, 3, 0, 1, 3 },
{ 3, 3, 3, 3, 1, 1, 0, 0 },
{ 1, 0, 1, 1, 3, 0, 3, 0 }
};


void main()
{
	int k = 0;
	int i, j;
	for (i = 0; i < 8; i++) {
		for (j = 0; j < 8; j++) {
			k = k + array[i][j];
		}
		printf("Команда k%d набрала всего %d очков\n", i+1, k);
		k = 0;
	}
}
